								<h3>ACT (Acceptance and Commitment Therapy)</h3>
								<p>ACT teaches a set of skills that enhance a client's psychological flexibility. With improved flexibility, a
                                    client experiences an increased ability to feel and think with openness, to attend voluntarily to more fully
                                    experiencing the present moment, and to discover and then move your life in new directions that are
                                    important to you. Clients find ACT helpful in treating issues like anxiety, depression, substance use
                                    problems, and relationship difficulty. ACT is about learning not to turn away from what is painful, instead
                                    turning toward your suffering in order to live a life full of meaning and purpose (Hayes, 2019).</p>
                                <a href="https://www.slideshare.net/gueste19b03/mindfulness-acceptance-powerpoint">Learn more about ACT </a>
                                <p></p>
								<h3>EMDR (Eye Movement Desensitization Reprocessing)</h3>
								<p>EMDR is a treatment protocol that has been in use for more than 30 years and has found more
                                    widespread application in the last decade. It has been endorsed by the APA (American Psychological
                                    Association) as an evidence-based method used in treating PTSD (post-traumatic stress disorder).
                                    EMDR helps clients process negative thoughts, feelings, and behaviors that have resulted from
                                    unprocessed difficult memories. Used effectively, EMDR reduces distress symptoms and creates a
                                    strengthened source of adaptive beliefs related to past trauma (Hensley, 2021).</p>
                                <a href="https://www.emdria.org/about-emdr-therapy/experiencing-emdr-therapy/">Overview of the experience of EMDR therapy<br></a>
                                <a href="https://www.emdria.org/wp-content/uploads/2021/08/8-Phases.border.pdf">Eight Phases of EMDR therapy</a>
                                <p></p>
                                <h3>MBB (Mind Body Bridging)</h3>
                                <p>MBB is a research-based protocol used to help clients learn how to quiet the overall activity in one's mind
                                    and body. Clients suffering from anxiety or difficulty coping with life stressors can find this treatment
                                    approach useful. This mind-body activity is referred to as the Identity System. Understanding the
                                    connection between one's thoughts and feelings and body tension is a core activity in MBB that allows
                                    clients to "rest the Identity System" to regain a sense of calm and control (Block, 2014).</p>
                                <a href="https://www.newharbinger.com/blog/professional/an-introduction-to-mind-body-bridging-the-i-system/?gclid=CjwKCAiAlrSPBhBaEiwAuLSDUAaYKm2-YlWojNOA0Bz7EdpSbGCMD4SZmjt5qFCZu_bfPNNtPlp_TxoC3XsQAvD_BwE">Introduction to MBB</a>
